    private void MakeGetRequest() {
        HttpClient client = new DefaultHttpClient();
        //String url = "http://google.co.kr";
        String url = "http://api.gokathon.hax0r.info/auth/send_push";
        HttpGet get = new HttpGet(url);
        try {
            HttpResponse response = client.execute(get);
            HttpEntity resEntity = response.getEntity();
            if(resEntity != null){
                String res = EntityUtils.toString(resEntity);
                Log.i("", res);
            }
        } catch (IOException e) { }
    }

    private void MakePostRequest(String token) {
        HttpClient httpClient = new DefaultHttpClient();
        HttpPost httpPost = new HttpPost("http://api.gokathon.hax0r.info/auth/send_push");

        //Post Data
        List<NameValuePair> nameValuePair = new ArrayList<NameValuePair>(2);
        //nameValuePair.add(new BasicNameValuePair("username", "test_user"));
        //nameValuePair.add(new BasicNameValuePair("id", "123456789"));
        //nameValuePair.add(new BasicNameValuePair("password", "123456789"));
        nameValuePair.add(new BasicNameValuePair("device_token", token));

        //device tokken
        //

        //Encoding POST data
        try {
            httpPost.setEntity(new UrlEncodedFormEntity(nameValuePair));
        } catch (UnsupportedEncodingException e) {
            // log exception
            e.printStackTrace();
        }

        //making POST request.
        try {
            HttpResponse response = httpClient.execute(httpPost);
            // write response to log
            Log.d("Http Post Response:", response.toString());
        } catch (ClientProtocolException e) {
            // Log exception
            e.printStackTrace();
        } catch (IOException e) {
            // Log exception
            e.printStackTrace();
        }
    }

